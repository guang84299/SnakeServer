package com.qianqi.server.game.model;

public class GDrop {

	private int num;
	private int x;
	private int y;
	
	public GDrop(int num, int x, int y) {
		super();
		this.num = num;
		this.x = x;
		this.y = y;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
